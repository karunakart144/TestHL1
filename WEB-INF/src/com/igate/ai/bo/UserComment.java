package com.igate.ai.bo;

public class UserComment {

	int commentId;
	String srchId;
	String usrComment;
	String voteLike;
	String voteDislike;
	String crtBy;
	String crtDate;
	String tagDesc;
	String tagName;
	
	public int getCommentId() {
		return commentId;
	}
	public void setCommentId(int commentId) {
		this.commentId = commentId;
	}
	public String getSrchId() {
		return srchId;
	}
	public void setSrchId(String srchId) {
		this.srchId = srchId;
	}
	public String getUsrComment() {
		return usrComment;
	}
	public void setUsrComment(String usrComment) {
		this.usrComment = usrComment;
	}
	public String getVoteLike() {
		return voteLike;
	}
	public void setVoteLike(String voteLike) {
		this.voteLike = voteLike;
	}
	public String getVoteDislike() {
		return voteDislike;
	}
	public void setVoteDislike(String voteDislike) {
		this.voteDislike = voteDislike;
	}
	public String getCrtBy() {
		return crtBy;
	}
	public void setCrtBy(String crtBy) {
		this.crtBy = crtBy;
	}
	public String getCrtDate() {
		return crtDate;
	}
	public void setCrtDate(String crtDate) {
		this.crtDate = crtDate;
	}
	public String getTagDesc() {
		return tagDesc;
	}
	public void setTagDesc(String tagDesc) {
		this.tagDesc = tagDesc;
	}
	public String getTagName() {
		return tagName;
	}
	public void setTagName(String tagName) {
		this.tagName = tagName;
	}

	
	
}
